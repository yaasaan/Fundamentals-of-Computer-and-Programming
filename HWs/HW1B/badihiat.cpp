#include <iostream>
using namespace std;

int main() {
	short int n;
	short int m;
	cin >> n;
	cin >> m;
	cout << n << "+" << m << "=" << n+m << "\n";
	cout << n << "-" << m << "=" << n-m << "\n";
	cout << n << "*" << m << "=" << n*m << "\n";
	cout << n << "/" << m << "=" << n/m << "\n";

	return 0;
}